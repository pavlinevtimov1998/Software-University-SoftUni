function greaterNumber(input) {

    let a = Number(input[0]);
    let b = Number(input[1]);

    if (a <= b) {
        console.log(b)
    } else {
        console.log(a)
    }

}
greaterNumber(["3", "3"])




function gNumber(something) {

    let a = Number(something[0]);
    let b = Number(something[1]);

    a <= b ? console.log(b) : console.log(a);

}
gNumber(["3", "3"])